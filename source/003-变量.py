# 字符串
name = "hello"
print(name)
name = "hello world"
print(name)
name = '''hello world "abc"'''
print(name)
name = """hello world 'abc'"""
print(name)
desc = """
    哈哈
    换行
    嘻嘻
"""
print(desc)

# 变量
# 变量名 = 值
# 变量可以随意赋值,不限制类型
# 变量不需要指定类型
# Python中变量名一般使用小写字母，单词间用下划线分割
a = 1
b = 2
print(a)
print(b)
a = "奇葩"
print(a)
person_age = 18
print(person_age)
product_price = 189.98
print(product_price)
